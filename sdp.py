""" WAP to find sum, difference, product of two numbers. """
x =float(input("Enter a number >>>"))
y =float(input("Enter another number >>>"))
sum = x + y
print("The sum of the two numbers entered is ", sum)
difference = x - y
print("The difference of the two numbers entered is ", difference)
product = x * y
print("The product of the two numbers entered is ", product)
